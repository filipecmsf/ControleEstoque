package com.android.controleestoque.VO;

public class DateVO {
	private static Integer dia;
	private static Integer mes;
	private static Integer ano;
	
	public Integer getDia() {
		return dia;
	}
	public static void setDia(Integer dia) {
		DateVO.dia = dia;
	}
	public Integer getMes() {
		return mes;
	}
	public static void setMes(Integer mes) {
		DateVO.mes = mes;
	}
	public Integer getAno() {
		return ano;
	}
	public static void setAno(Integer ano) {
		DateVO.ano = ano;
	}
	
	
	
}
